#!/usr/bin/env node
/**
 * Empirical buildRequirements extractor.
 *
 * For each installation type in src/data/installationTypes.ts, find completed
 * projects in colony-data.json that match (by completedStationType or
 * stationType), compute MEDIAN commodity quantities across them, and emit a
 * TS-paste-ready fragment.
 *
 * Median (not mean) because Frontier's depot quantities are mostly stable per
 * type, but occasional outliers (early builds with different ratios) skew the
 * mean. Median is robust.
 *
 * Output goes to stdout — review before pasting into installationTypes.ts'
 * BUILD_REQUIREMENTS map.
 *
 * Usage: node tools/backfill-installation-builds.mjs
 *        node tools/backfill-installation-builds.mjs --type military_outpost
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const COLONY_DATA = path.join(ROOT, 'colony-data.json');

const args = process.argv.slice(2);
const filterIdx = args.indexOf('--type');
const filterType = filterIdx >= 0 ? args[filterIdx + 1] : null;

const data = JSON.parse(fs.readFileSync(COLONY_DATA, 'utf8'));
const projects = (data.projects || []).filter((p) => p.status === 'completed' && p.commodities && p.commodities.length > 0);

// Group projects by installation type id. Use completedStationType first
// (refined), fall back to stationType (raw journal).
const byType = new Map();
for (const p of projects) {
  const type = p.completedStationType || p.stationType;
  if (!type) continue;
  if (filterType && type !== filterType) continue;
  if (!byType.has(type)) byType.set(type, []);
  byType.get(type).push(p);
}

function median(values) {
  if (values.length === 0) return 0;
  const sorted = [...values].sort((a, b) => a - b);
  const mid = Math.floor(sorted.length / 2);
  return sorted.length % 2 === 0 ? Math.round((sorted[mid - 1] + sorted[mid]) / 2) : sorted[mid];
}

const sortedTypes = [...byType.entries()].sort((a, b) => b[1].length - a[1].length);

console.log('// =============================================================================');
console.log('// Empirical buildRequirements derived from completed projects in colony-data.json');
console.log(`// Generated by tools/backfill-installation-builds.mjs at ${new Date().toISOString()}`);
console.log('// Project counts shown per type; median quantities across observed builds.');
console.log('// =============================================================================');
console.log('');

for (const [type, list] of sortedTypes) {
  // Aggregate: commodityId → array of requiredQuantity across projects
  const commodityQuantities = new Map();
  for (const p of list) {
    for (const c of p.commodities) {
      if (!commodityQuantities.has(c.commodityId)) commodityQuantities.set(c.commodityId, []);
      commodityQuantities.get(c.commodityId).push(c.requiredQuantity);
    }
  }

  // Filter: keep commodities that appear in MOST builds of this type
  const threshold = Math.ceil(list.length / 2);
  const entries = [];
  for (const [commodityId, quantities] of commodityQuantities) {
    if (quantities.length < threshold) continue; // appears in less than half of builds — likely a special-case mat
    entries.push({ commodityId, quantity: median(quantities), seenIn: quantities.length });
  }
  entries.sort((a, b) => b.quantity - a.quantity);

  const total = entries.reduce((s, e) => s + e.quantity, 0);
  console.log(`  // ${type} — observed ${list.length} build(s), median total ~${total.toLocaleString()}t`);
  console.log(`  ${type}: [`);
  for (const e of entries) {
    console.log(`    { commodityId: '${e.commodityId}', quantity: ${e.quantity} }, // seen in ${e.seenIn}/${list.length}`);
  }
  console.log('  ],');
  console.log('');
}

console.log(`Total: ${sortedTypes.length} installation types observed across ${projects.length} completed projects.`);
if (filterType) console.log(`(filtered to type=${filterType})`);
